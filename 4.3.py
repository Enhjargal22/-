class Зоомагазин:
    def __init__(self):
        self.животные = []

    def добавить_животное(self, животное):
        self.животные.append(животное)

    def самая_дорогая_порода(self):
        if not self.животные:
            return None

        самое_дорогое_животное = max(self.животные, key=lambda животное: животное.стоимость)
        return самое_дорогое_животное.порода


class Животное:
    def __init__(self, порода, стоимость):
        self.порода = порода
        self.стоимость = стоимость

    def передвижение(self):
        return "Двигается с использованием общего способа передвижения животных"


class Рыбы(Животное):
    def передвижение(self):
        return "Плавает"


class Птицы(Животное):
    def передвижение(self):
        return "Летает"


# Пример использования классов
if __name__ == "__main__":
    зоомагазин = Зоомагазин()

    # Добавляем различные животные
    зоомагазин.добавить_животное(Рыбы("Золотая рыбка", 30))
    зоомагазин.добавить_животное(Рыбы("Тунец", 25))
    зоомагазин.добавить_животное(Птицы("Попугай", 300))
    зоомагазин.добавить_животное(Птицы("Орёл", 200))

    # Выводим данные о самой дорогой породе
    самая_дорогая_порода = зоомагазин.самая_дорогая_порода()
    if самая_дорогая_порода:
        print(f"Самая дорогая порода: {самая_дорогая_порода}")
    else:
        print("В зоомагазине нет животных.")
